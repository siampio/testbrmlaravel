     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product = new Inventory();
        $product->lot_number = $request->input('lote_number');
        $product->id_prod = $request->input('id_product');
        $product->cantidad_prod = $request->input('cantidad_prod');
        $product->fecha_ven = $request->input('date_ven');
        $product->precio_prod = $request->input('precio_prod');

        $product->save();

        return back()->withInput();
    }

